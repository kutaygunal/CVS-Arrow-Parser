"""
experiment_csv.py  — the ONLY file the agent modifies.
Tunable parameters and experiment logic for optimizing GPU CSV parser throughput.
"""
import os
import sys
import time

# ---------------------------------------------------------------------------
# Imports from fixed infrastructure
# ---------------------------------------------------------------------------
SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
sys.path.insert(0, SCRIPT_DIR)
from prepare_csv import build_project, evaluate_current

# ---------------------------------------------------------------------------
# Tunable parameters
# ---------------------------------------------------------------------------
TUNABLES = {
    # Kernel launch configuration for parsing kernels
    "THREADS_PER_BLOCK": 256,

    # Kernel launch configuration for mark_newlines
    "MARK_THREADS_PER_BLOCK": 256,

    # Kernel launch configuration for compact_newlines
    "COMPACT_THREADS_PER_BLOCK": 256,

    # Parser strategy: 1 = warp-per-row, 0 = thread-per-row (fallback)
    "USE_WARP_PARSE": 1,

    # Use fast branchless parsers: 1 = fast, 0 = safe with validation
    "USE_FAST_PARSER": 1,

    # Warp-level field scan capacity
    "MAX_FIELDS_PER_WARP": 32,

    # Use 128-bit uint4 loads for warp delimiter scanning
    "USE_WIDE_LOADS": 0,

    # Multi-stream chunking (set to 1 to disable)
    "NUM_STREAMS": 1,
    "MIN_CHUNK_SIZE_MB": 32,
}

# ---------------------------------------------------------------------------
# Source modification helpers
# ---------------------------------------------------------------------------
ROOT_DIR = os.path.abspath(os.path.join(SCRIPT_DIR, ".."))
TUNEABLE_H = os.path.join(ROOT_DIR, "include", "tuneable_params.h")

def write_tuneable_h():
    """Rewrite tuneable_params.h from TUNABLES dict."""
    lines = [
        "#pragma once",
        "// Autoresearch tunable parameters for gpu_csv_parser.cu",
        "// This file is auto-generated by the experiment script.",
        "",
        f"#define TUNE_THREADS_PER_BLOCK {TUNABLES['THREADS_PER_BLOCK']}",
        f"#define TUNE_MARK_THREADS_PER_BLOCK {TUNABLES['MARK_THREADS_PER_BLOCK']}",
        f"#define TUNE_COMPACT_THREADS_PER_BLOCK {TUNABLES['COMPACT_THREADS_PER_BLOCK']}",
        f"#define TUNE_USE_WARP_PARSE {TUNABLES['USE_WARP_PARSE']}",
        f"#define TUNE_USE_FAST_PARSER {TUNABLES['USE_FAST_PARSER']}",
        f"#define TUNE_MAX_FIELDS_PER_WARP {TUNABLES['MAX_FIELDS_PER_WARP']}",
        f"#define TUNE_USE_WIDE_LOADS {TUNABLES['USE_WIDE_LOADS']}",
        f"#define TUNE_NUM_STREAMS {TUNABLES['NUM_STREAMS']}",
        f"#define TUNE_MIN_CHUNK_SIZE_BYTES ({TUNABLES['MIN_CHUNK_SIZE_MB']} * 1024 * 1024)",
        "",
    ]
    with open(TUNEABLE_H, "w") as f:
        f.write("\n".join(lines))
    print(f"Wrote {TUNEABLE_H}")

def run_experiment():
    """
    Full experiment pipeline:
      1. Write tuneable header
      2. Build
      3. Benchmark repeatedly for fixed time budget
      4. Print summary
    """
    t_start = time.time()
    write_tuneable_h()

    ok, build_ms = build_project()
    if not ok:
        print("---")
        print("throughput_mib_s: 0.000000")
        print("build_seconds:    0.0")
        print("status:           crash")
        print("---")
        return False

    print(f"Build took {build_ms:.0f} ms")
    res = evaluate_current()
    total_seconds = time.time() - t_start

    if res is None:
        print("---")
        print("throughput_mib_s: 0.000000")
        print("build_seconds:    0.0")
        print("status:           crash")
        print("---")
        return False

    print("---")
    print(f"throughput_mib_s: {res['mean_mib_s']:.6f}")
    print(f"stdev_mib_s:      {res['stdev_mib_s']:.6f}")
    print(f"n_runs:           {res['n_runs']}")
    print(f"build_seconds:    {build_ms/1000.0:.2f}")
    print(f"total_seconds:    {total_seconds:.1f}")
    print("---")
    return True


if __name__ == "__main__":
    run_experiment()
